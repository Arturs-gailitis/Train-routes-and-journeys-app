    i NUMBER := 1000;
    max_route NUMBER;
BEGIN
    SELECT NVL(MAX(ROUTE_ID), 0) INTO max_route FROM MARŠRUTI;
    WHILE i <= max_route LOOP
        BEGIN
            EXECUTE IMMEDIATE 'DROP TABLE "MARŠRUTI_AR_' || i || '_ID"';
        EXCEPTION
            WHEN OTHERS THEN NULL;
        END;
        i := i + 1000;
    END LOOP;
EXCEPTION
    WHEN OTHERS THEN NULL;
END;
