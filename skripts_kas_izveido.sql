                '  EXECUTE IMMEDIATE ''DROP TABLE DIENU_SKATS''; ' ||
                'EXCEPTION ' ||
                '  WHEN OTHERS THEN ' ||
                '    IF SQLCODE != -942 THEN RAISE; END IF; ' ||
                'END;';
    EXECUTE IMMEDIATE drop_sql;
    sql_text := 'CREATE TABLE DIENU_SKATS (
        ID NUMBER GENERATED ALWAYS AS IDENTITY,
        DIENA VARCHAR2(20),
        PIEEJAMI NUMBER,
        NEPIEEJAMI NUMBER
    )';
